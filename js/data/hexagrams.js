// 根据上下卦获取64卦
DivinationData.getHexagram = function(upperTrigram, lowerTrigram) {
  var index = upperTrigram * 8 + lowerTrigram
  return DivinationData.hexagrams[index] || DivinationData.hexagrams[0]
}

// 获取特定方向的解读
DivinationData.getReading = function(hexagram, category) {
  switch (category) {
    case 'career':
      return hexagram.career
    case 'love':
      return hexagram.love
    case 'wealth':
      return hexagram.wealth
    case 'health':
      return hexagram.health
    case 'study':
      return hexagram.study || hexagram.career
    case 'social':
      return hexagram.social || hexagram.love
    case 'travel':
      return hexagram.travel || hexagram.health
    case 'general':
      return hexagram.general || hexagram.career
    default:
      return hexagram.general || hexagram.career
  }
}
